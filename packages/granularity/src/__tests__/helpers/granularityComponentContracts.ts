import type { GranularityComponentName, GranularityComponentSelection } from '../../registry/components'

type GranularityComponentContract = {
  name: GranularityComponentName
  config: GranularityComponentConfig<GranularityComponentName>
  safelist: readonly string[]
  dependencies: readonly GranularityComponentName[]
}

type GranularityComponentCssContract = {
  files: readonly {
    pattern: RegExp
    mustBeFileUrl?: boolean
  }[]
  assetNames: readonly string[]
}

type GranularityStyleAssetContract = {
  fileName: string
  components: GranularityComponentSelection
}

function hasStyleAssetFileName<T extends { styleAssetFileName: string | null }>(
  config: T,
): config is T & { styleAssetFileName: string } {
  return Boolean(config.styleAssetFileName)
}

